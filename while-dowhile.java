// While
public class Main{
	 public static void main(String[] args) {
		int contador = 10;

		while (contador > 0){
			System.out.println(contador);
			// contador = contador - 1 o contador--;
		}
	}
}

// Dowhile
public class Main{
	 public static void main(String[] args) {
		int contador = 2;

		do{
			System.out.println(contador);
			// contador = contador - 1 o contador--;
		} while (contador > 2);
	}
}